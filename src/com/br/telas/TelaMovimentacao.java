    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() > -1) {
            TelaMovimentacaoCadastro t = new TelaMovimentacaoCadastro(this, true);
            t.setInserir(false);
            if (lista.get(tabela.getSelectedRow()).getIdCategoria().getTipo().equals("Despesa")) {
                t.setDespesa(true);
                t.atualizarCombo();
                t.preencherCampos(lista.get(tabela.getSelectedRow()));
                t.setVisible(true);
            } else {
                t.setDespesa(false);
                t.atualizarCombo();
                t.preencherCampos(lista.get(tabela.getSelectedRow()));
                t.setVisible(true);
            }
        } else {
            Msg.alert(this, "Selecione um registro!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

