    private void mudarCorLinha() {
        tabela.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                if (table.getValueAt(row, 2).equals("Despesa")) {
                    setForeground(Color.red);
                } else {
                    setForeground(Color.green);
                }
                return this;
            }
        });
    }

    protected void atualizarTabela() {
        try {
            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.setNumRows(0);
            lista = new MovimentacaoDao().getListaData(txPesquisa.getText(), txDataInicial.getDate(), txDataFinal.getDate());
            double despesa = 0, renda = 0;
            for (Movimentacao c : lista) {
                if (c.getIdCategoria().getTipo().equals("Despesa")) {
                    despesa += c.getValor();
                } else {
                    renda += c.getValor();
                }
                model.addRow(new Object[]{
                    c.getIdMovimentacao(), Utils.convertData(c.getData()), c.getIdCategoria().getTipo(), Utils.convertDouble(c.getValor()), c.getIdCategoria().getDescricao()
                });
            }
            txDespesa.setText(Utils.convertDouble(despesa));
            txRenda.setText(Utils.convertDouble(renda));
            txSaldo.setText(Utils.convertDouble(renda - despesa));
        } catch (Exception e) {
            e.printStackTrace();
            Msg.erro(this, "Erro ao atualizar a tabela\n Erro: " + e.getMessage());
        }
    }

